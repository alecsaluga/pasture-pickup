// Generate all possible SEO page paths
export function generateSEOPaths() {
  const paths: string[] = [];
  
  // State level pages
  US_STATES.forEach(state => {
    const stateSlug = state.name.toLowerCase().replace(/\s+/g, '-');
    paths.push(`/${stateSlug}`);
    
    // State + service pages
    LIVESTOCK_SERVICES.forEach(service => {
      paths.push(`/${stateSlug}/${service}`);
    });
    
    // City level pages
    state.major_cities.forEach(city => {
      const citySlug = city.toLowerCase().replace(/\s+/g, '-');
      paths.push(`/${stateSlug}/${citySlug}`);
      
      // City + service pages
      LIVESTOCK_SERVICES.forEach(service => {
        paths.push(`/${stateSlug}/${citySlug}/${service}`);
      });
    });
  });
  
  return paths;
}

export function getStateBySlug(slug: string) {
  return US_STATES.find(state => 
    state.name.toLowerCase().replace(/\s+/g, '-') === slug
  );
}

export function getCityInState(stateSlug: string, citySlug: string) {
  const state = getStateBySlug(stateSlug);
  if (!state) return null;
  
  const city = state.major_cities.find(city => 
    city.toLowerCase().replace(/\s+/g, '-') === citySlug
  );
  
  return city ? { city, state } : null;
}

export function getServiceBySlug(slug: string) {
  return LIVESTOCK_SERVICES.includes(slug) ? slug : null;
}